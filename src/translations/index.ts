export const t = (language: Language, key: string): string => {
  const keys = key.split(".");
  let value: any = translations[language];
  
  for (const k of keys) {
    value = value[k];
    if (value === undefined) {
      console.warn(`Translation key not found: ${key}`);
      return key;
    }
  }
  
  return value;
};
